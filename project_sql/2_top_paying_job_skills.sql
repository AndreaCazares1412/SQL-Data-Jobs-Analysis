--Question: What skills are required for the top-paying data analyst jobs?


--Subquery
SELECT 
    top_ten_highest_paying.*,
    skills_dim.skills
FROM(
SELECT 
    job_id, 
    job_title,
    salary_year_avg, 
    c.name AS company
FROM job_postings_fact AS jpf
LEFT JOIN company_dim AS c ON  jpf.company_id = c.company_id
WHERE 
    job_work_from_home = True AND
    job_location = 'Anywhere' AND 
    salary_year_avg IS NOT NULL AND 
    job_title_short = 'Data Analyst'
ORDER BY salary_year_avg DESC
LIMIT 10
) AS top_ten_highest_paying 
JOIN skills_job_dim ON top_ten_highest_paying.job_id = skills_job_dim.job_id
JOIN skills_dim ON skills_job_dim.skill_id = skills_dim.skill_id;


-- CTE

WITH top_ten_highest_paying AS (
    SELECT 
    job_id, 
    job_title,
    salary_year_avg, 
    c.name AS company
FROM job_postings_fact AS jpf
LEFT JOIN company_dim AS c ON  jpf.company_id = c.company_id
WHERE 
    job_work_from_home = True AND
    job_location = 'Anywhere' AND 
    salary_year_avg IS NOT NULL AND 
    job_title_short = 'Data Analyst'
ORDER BY salary_year_avg DESC
LIMIT 10
)

SELECT 
    top_ten_highest_paying.*,
    skills_dim.skills
FROM top_ten_highest_paying
INNER JOIN skills_job_dim ON top_ten_highest_paying.job_id = skills_job_dim.job_id
INNER JOIN skills_dim ON skills_job_dim.skill_id = skills_dim.skill_id
ORDER BY salary_year_avg DESC;


